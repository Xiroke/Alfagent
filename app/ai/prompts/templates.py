def build_user_prompt(
    *,
    user_message: str,
    rag_context: str,
    company_profile: str | None = None,
) -> str:
    profile_block = company_profile or "Профиль компании не передан."
    return (
        f"### Профиль компании\n{profile_block}\n\n"
        f"### Контекст из базы знаний (RAG)\n{rag_context}\n\n"
        f"### Вопрос пользователя\n{user_message}\n\n"
        "Ответь строго по роли агента. Если контекста недостаточно — укажи это явно."
    )


def get_agent_system_prompt(agent: AgentType) -> str:
    return AGENT_SYSTEM_PROMPTS[agent]
