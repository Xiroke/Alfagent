async def main() -> None:
    logging.basicConfig(level=logging.INFO)
    settings = get_settings()
    if not settings.llm_configured:
        raise SystemExit("Set API_KEY before seeding")

    init_db(settings)
    client = LLMClient(settings)
    await client.start()
    try:
        async with session_scope() as session:
            service = EmbeddingService(session, client, settings)
            total = 0
            for doc in SEED_DOCUMENTS:
                chunks = await service.ingest_document(
                    source=doc["source"],
                    category=doc["category"],
                    content=doc["content"],
                    title=doc["title"],
                )
                total += len(chunks)
                logger.info("Seeded %s -> %s chunks", doc["source"], len(chunks))
            logger.info("Done. Total chunks: %s", total)
    finally:
        await client.close()
        await dispose_db()


if __name__ == "__main__":
    asyncio.run(main())
